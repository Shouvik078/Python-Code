a=[1,2,3]
print(a)
# adding element in last
a.append(7)
print(a)

# adding element in particular position
a.insert(0,9)
print(a)

# sorting list
a.sort()
print(a)

# removing element from last
a.pop()
print(a)

# reversing the list
a.reverse()
print(a)

# return index of that particular element
print(a.index(3))

print(a.count(2))